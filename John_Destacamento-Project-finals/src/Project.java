    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
           
        Double num1 = null;
        while (num1 == null) {
            String input = JOptionPane.showInputDialog("Enter first number:");
            try {
                num1 = Double.parseDouble(input);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid input! Please enter a number.");
            }
        }

        String operator;
        while (true) {
            operator = JOptionPane.showInputDialog("Enter operator (+, -, *, /):");
            if (operator != null && operator.matches("[+\\-*/]")) {
                break;
            } else {
                JOptionPane.showMessageDialog(null, "Invalid operator! Please try again.");
            }
        }

        Double
                num2 = null;
        while (num2 == null) {
            String input = JOptionPane.showInputDialog("Enter second number:");
            try {
                num2 = Double.parseDouble(input);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid input! Please enter a number.");
            }
        }

        String result;
        switch (operator) {
            case "+":
                result = String.valueOf(num1 + num2);
                break;
            case "-":
                result = String.valueOf(num1 - num2);
                break;
            case "*":
                result = String.valueOf(num1 * num2);
                break;
            case "/":
                if (num2 != 0) {
                    result = String.valueOf(num1 / num2);
                } else {
                    result = "Cannot divide by zero!";
                }
                break;
            default:
                result = "Invalid operation";
        }

        JOptionPane.showMessageDialog(null, "Result: " + result);
        
        
    }//GEN-LAST:event_jButton20ActionPerformed

