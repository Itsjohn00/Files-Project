    private static void fizzBuzzRun() {
        int toggle = 0;
        if (toggle == 1) {
            JOptionPane.showMessageDialog(null, "Output cleared");
            toggle = 0;
            return;
        }
        
        StringBuilder output = new StringBuilder();
        for (int i = 1; i <= 50; i++) {
            if (i % 3 == 0 && i % 5 == 0) {
                output.append("FizzBuzz\n");
            } else if (i % 3 == 0) {
                output.append("Fizz\n");
            } else if (i % 5 == 0) {
                output.append("Buzz\n");
            } else {
                output.append(i).append("\n");
            }
      
        }
        
        JOptionPane.showMessageDialog(null, 
            new JScrollPane(new JTextArea(output.toString())), 
            "FizzBuzz Results", 
            JOptionPane.PLAIN_MESSAGE);
        
        toggle = 1;
    
    }//GEN-LAST:event_fizzbuzzActionPerformed

